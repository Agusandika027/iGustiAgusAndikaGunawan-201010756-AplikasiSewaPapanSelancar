    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
        
        String tgl = cTanggal.getSelectedItem().toString();
        String bln = cBulan.getSelectedItem().toString();
        String thn = cTahun.getSelectedItem().toString();
        String masuk = thn+"-"+bln+"-"+tgl;
        
        String id_papan = cPapan.getSelectedItem().toString();
        String id_penyewa = cPenyewa.getSelectedItem().toString();
        
        if (tIdsewa.getText().equals("") || masuk == "" ){
            JOptionPane.showMessageDialog(null, "Field Tidak Boleh Kosong");
        } else {
            try {
               int s;
               String sql = "INSERT INTO sewa VALUES ('"+tIdsewa.getText()+"', '"+id_papan+"', '"+id_penyewa+"'  , '"+masuk+"', '"+tLama.getText()+"', '"+tTotal.getText()+"')";
               st = conn.createStatement();
               s = st.executeUpdate(sql);
               if (s == 1){
                JOptionPane.showMessageDialog(null, "Sukses");
                clear();
                tampil();
               }
           } catch (Exception e){
                System.out.println(e.toString());
           }    
        }
    }//GEN-LAST:event_bSimpanActionPerformed

    private void tableSewaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSewaMouseClicked
        // TODO add your handling code here:
        
        int baris = tableSewa.getSelectedRow();
	tIdsewa.setText(tableSewa.getValueAt(baris, 1).toString());
	cPapan.setSelectedItem(tableSewa.getValueAt(baris, 2).toString());
        cPenyewa.setSelectedItem(tableSewa.getValueAt(baris, 3).toString());
        
        // tanggal masuk
        cBulan.setSelectedItem(tableSewa.getValueAt(baris, 4).toString().substring(8, 10));
        cTanggal.setSelectedItem(tableSewa.getValueAt(baris, 4).toString().substring(5, 7));
        cTahun.setSelectedItem(tableSewa.getValueAt(baris, 4).toString().substring(0, 4));
        
        tLama.setText(tableSewa.getValueAt(baris, 5).toString());
        tTotal.setText(tableSewa.getValueAt(baris, 6).toString());
    }//GEN-LAST:event_tableSewaMouseClicked

