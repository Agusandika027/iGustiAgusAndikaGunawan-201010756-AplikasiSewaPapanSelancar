    public void tampil(){
        Object [] baris = {"NO", "ID Sewa", "ID Papan", "ID Penyewa ", "Tanggal Sewa","Lama Sewa" ,"Harga Total"};
        tabMode = new DefaultTableModel(null, baris);
        tableSewa.setModel(tabMode);
        try {
                String sql = "SELECT * FROM sewa";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                int no = 0;
                while (rs.next()){
                        no++;
                        String id = rs.getString("id_sewa");
                        String id_papan = rs.getString("id_papan");
                        String id_penyewa = rs.getString("id_penyewa");
                        String tgl = rs.getString("tanggal_sewa");
                        String lama = rs.getString("lama_sewa");
                        String harga = rs.getString("harga_total");

                        Object [] data = {no,id,id_papan,id_penyewa,tgl,lama,harga};
                        tabMode.addRow(data);
                }
        } catch (Exception e){
                System.out.println(e.toString());
        }
    }
    
